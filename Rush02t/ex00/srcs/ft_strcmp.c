#include "header.h"

int		ft_strcmp(char *s1, char *s2)
{
	char	cmp;

	while ((*s1 && *s2) && *s1 == *s2)
	{
		s1++;
		s2++;
	}
	cmp = *s1 - *s2;
	return ((int)cmp);
}
