int		ft_compact(char **tab, int length)
{
	int		index;
	int		i;

	index = 0;
	i = 0;
	while (i < length)
	{
		if (tab[i] != '\0')
			tab[index++] = tab[i];
		i++;
	}
	return (index);
}
