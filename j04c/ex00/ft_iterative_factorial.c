int		ft_iterative_factorial(int nb)
{
	int		f;
	int		i;

	if (nb == 0)
	{
		return (1);
	}
	if (nb < 0 || nb > 12)
	{
		return (0);
	}
	else
	{
		i = 1;
		f = 1;
		while (f <= nb)
		{
			i = f * i;
			f++;
		}
		return (i);
	}
}
