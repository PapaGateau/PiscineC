int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while ((s1[i] == s2[i]) && (s1[i] != '\0'))
	{
		if (i + 1 == n)
		{
			return (0);
		}
		i++;
	}
	if (s1[i] != s2[i])
	{
		return ((int)s1[i] - (int)s2[i]);
	}
	return (0);
}
