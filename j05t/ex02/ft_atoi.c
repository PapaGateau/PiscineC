int		ft_atoi(char *str)
{
	int		i;
	int		is_neg;
	int		nb;

	is_neg = 0;
	i = 0;
	nb = 0;
	while ((str[i] == ' ') || (str[i] == '\t') || (str[i] == '\n')
			|| (str[i] == '\v') || (str[i] == '\f') || (str[i] == '\r'))
		i++;
	if (str[i] == 45)
		is_neg = 1;
	if ((str[i] == 43) || (str[i] == 45))
		i++;
	while ((str[i] > 47) && (str[i] < 58))
	{
		nb *= 10;
		nb += ((int)str[i] - 48);
		i++;
	}
	if (is_neg == 1)
		return (-nb);
	else
		return (nb);
}
