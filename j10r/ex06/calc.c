int		ft_add(int n1, int n2)
{
	return (n1 + n2);
}

int		ft_sub(int n1, int n2)
{
	return (n1 - n2);
}

int		ft_mul(int n1, int n2)
{
	return (n1 * n2);
}

int		ft_div(int n1, int n2)
{
	return (n1 / n2);
}

int		ft_mod(int n1, int n2)
{
	return (n1 % n2);
}
